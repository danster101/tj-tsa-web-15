<h3>History</h3>

<h4>Pre-2012</h4>

<p>TJ had some attempts to begin TSA chapters in the past. However, most of them only lasted a single year before disbanding.</p>

<h4>2012-2013</h4>

<p>The current TJHSST TSA Chapter was founded in 2012 by Lucas Lin and Kyle Herndon. Danial Hussain and Matthew Savage aided in establishing the chapter and making sure members were ready for the regional, state, and national conferences. The chapter was mostly filled with members who were graduates from the Rachel Carson Middle School TSA Chapter. That year, the chapter had 25 members.</p>

<p>The chapter's first tests of its ability came in 2013 with the Northern Regional Fair and Technosphere. TJ TSA took home 13 each at the Regional Fair and at Technosphere. TJ's Rohan Pandit won <i>Most Competitive Technosphere Participant</i>, and the school as a whole won the <i>Arvid W. Van Dyke Outstanding School Award</i>.</p>

<p>Seven of TJ TSA's members made the trip to Orlando for the National Conference in June 2013. The team had a handful of finalist finishes and a single trophy, won by Matthew Barbano for Future Technology Teacher.</p>

